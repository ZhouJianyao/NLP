def sif_whole_text(doc, model, a=1e-3, unlisted_word_freq=0.0001):
    '''
    给定一篇文章，把所有的句子连一起当成一个长句，
    返回：这个长句的句向量（1，200）
    
    注：如果输入为空，则会返回零向量1x200
    '''

    doc_split = []
    for i in wash_and_split(doc):
        doc_split.extend(i)
    return SIF([doc_split], model, a, unlisted_word_freq)


def get_sentence_lookup_table(content, model, a=1e-3, unlisted_word_freq=0.0001):
    """
    因为最后需要计算相似度，并进行排序并取出句子，所以这里要构建一个句子-句向量的对应表
    这里使用元组，即(分词的句子，句子的array)
    """
    after_content = wash_and_split(content)
    vectors = SIF(after_content, model, a, unlisted_word_freq)
    return [(sent, vec) for sent, vec in zip(after_content, vectors)]


def get_similarity_lookup_table(sentence_lookup_table, v_content, knn_n, knn_weights):
    '''
    Input: 
        sentence_lookup_table：每句话的句向量，查询表的形式
        v_content：文章当作长句的句向量
        similarity : 相似度函数cosine
        knn_n : 左右多少个平滑，(left, right)
        knn_weights : 权重表
    Output:
        similarity_lookup_table：句子与文章的相似度值组成的向量，查询表的形式
    '''
    similarity_lookup_table = [(sent, cosine_similarity(v_sent.reshape(1, -1), v_content.reshape(1, -1))) for sent, v_sent in sentence_lookup_table]

    # KNN平滑
    assert sum(knn_n)+1 == len(knn_weights)
    
    knn_weights = np.array(knn_weights)
    # 左右填充0，为了滑动窗口方便
    temp_ = np.array([0]*knn_n[0] + [i[1] for i in similarity_lookup_table] + [0]*knn_n[1])
    smoothed = []
    for i in range(len(temp_)-sum(knn_n)):
        smoothed.append(np.dot(knn_weights, temp_[i:i+sum(knn_n)+1]))
        
    assert len(smoothed) == len(similarity_lookup_table)
        
    similarity_lookup_table_smoothed = [(i[0], j) for i, j in zip(similarity_lookup_table, smoothed)]
    
    return similarity_lookup_table_smoothed


def top_n(smoothed, n=5, remain_sentence_order=True):
    '''
    先对原序列增加一个序号值，后面如果要还原句子顺序，就对序号值重新排序即可
    '''
    x = [(sent, sim, i) for (sent, sim), i in zip(smoothed, range(1, len(smoothed)+1))]
    x.sort(key=lambda i: i[1], reverse=True)
    if n == 'auto':
        x = x[:count_sentence(len(x))]
    else: 
        x = x[:n]
    
    if remain_sentence_order:
        x.sort(key=lambda i: i[2])
    return "|".join(["".join(i[0]) for i in x])


def count_sentence(x):
    '''
    输入文章长度 - 输出摘要长度 的 映射函数
    '''
    if x <= 20:
        n = 3
    elif 20 < x < 35:
        n = 5
    elif 35 <= x < 50:
        n = 8
    else:
        n = 10
    return n


def get_abstract(content):

    v_doc = sif_whole_text(content, model=config.model, a=config.a, unlisted_word_freq=config.unlisted_word_freq)
    # 计算V_sentences查询表，句子和向量一一对应，每个句向量都为 200 维
    sentence_lookup_table = get_sentence_lookup_table(content, model=config.model, a=config.a, unlisted_word_freq=config.unlisted_word_freq)
    # 计算V相似度查表，句子和相似度一一对应
    sim_table_smoothed = get_similarity_lookup_table(sentence_lookup_table, v_doc, knn_n=config.knn_n,
                                                     knn_weights=config.knn_weights)
    # get Top_N
    result = top_n(sim_table_smoothed, n=config.top_n, remain_sentence_order=config.remain_sentence_order)

    return result


