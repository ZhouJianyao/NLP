'''
输入：Content
输出：Summary

方法：

1. 对Content仅分词，然后分别计算向量V_text

2. 对Content分词分句，然后计算出每一句话的向量V_sentence(i)
            
3. 把2处理成相似度表(与全文的相似度)，并做KNN平滑

4. 取 Top_N 作为摘要
'''
